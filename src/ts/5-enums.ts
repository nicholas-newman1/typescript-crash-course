/* Enum: Having some readable constants that you want represented as a number
(ex: days of the week, compass directions, etc). This type is added by 
TypeScript and does not exist in vanilla JS. Enum is a custom type and by
convention the first letter is capitalized. It is also a convention to
uppercase the values in an enum. */
enum Directions {
  NORTH,
  SOUTH,
  EAST,
  WEST,
}

